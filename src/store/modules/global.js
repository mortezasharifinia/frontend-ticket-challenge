// ##################################################################################
// ################################### State ########################################
// ##################################################################################
const state = {
  loading: false,
  errors: null,
};

// ##################################################################################
// ################################## Getters #######################################
// ##################################################################################
const getters = {
  getLoading(state) {
    return state.loading;
  },
  getErrors(state) {
    return state.errors;
  },
};

// ##################################################################################
// ################################## Mutations #####################################
// ##################################################################################
const mutations = {
  SET_LOADING(state, loading) {
    state.loading = loading;
  },
  SET_ERRORS(state, errors) {
    state.errors = errors;
  },
};

// ##################################################################################
// ################################### Actions ######################################
// ##################################################################################
const actions = {};

export default {
  namespaced: true,
  state,
  getters,
  mutations,
  actions,
};
